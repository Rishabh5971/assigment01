// Write a c++ program to find size of basic data types.

#include<iostream>
using namespace std;
int main(){
	cout<<"The size of (char) : "<<sizeof(char)<<" byte"<<endl;
	cout<<"The size of (short) : "<<sizeof(short)<<" byte"<<endl;
	cout<<"The size of (int) : "<<sizeof(int)<<" byte"<<endl;
	cout<<"The size of (long) : "<<sizeof(long)<<" byte"<<endl;
	cout<<"The size of (long long) : "<<sizeof(long long)<<" byte"<<endl;
	cout<<"The size of (float) : "<<sizeof(float)<<" byte"<<endl;
	cout<<"The size of (double) : "<<sizeof(double)<<" byte"<<endl;
	cout<<"The size of (long double) : "<<sizeof(long double)<<" byte"<<endl;
	cout<<"The size of (bool) : "<<sizeof(bool)<<" byte"<<endl;
	
	return 0;
}